            <p>I received this product as a free sample to review.</p>
            <p>Overall, I would give this vacuum very high marks - with a few exceptions.</p>
            <p>First it was very easy to put together. No tools required. It was all `snap` into place. Very much appreciated as it required no tools. Instructions were very easy to understand. This was a huge plus.</p>
            <p>On carpet it is excellent! It was much easier to use on carpet than my Oreck. It was easier to push, easier to maneuver and got in tight spots better. It rolls over unevern surfaces really well - hardly feel it as it moves. Also, the amount of dirt this machine picks up is unbelievable! The canister had a lot of dirt in it and this was after using my Oreck the day before. Makes you think about all of the dirt you are leaving on your floor with a normal vacuum. I give it A+++ on carpet.</p>
            <p>Canister was easy to unlock and unload. No issues there.</p>
            <p>Hard to tell on hardwood. We have a lot of hardwood and not sure how it performed compared to our Oreck. Switching from carpet to hardwood was easy - just pushing a button. Rolls easy without any issues.</p>
            <p>A few things I did not like. These are compared to my Oreck. It is heavier. Lifting up and down the stairs was harder than my Oreck. Not sure it is any lighter than other vacuums of comparable size but if you are lifting up and down stairs and don`t have strong arms you will fell the weight.</p>
            <p>Using the extension tube was also a bit awkward. I used it on the steps and when I had the vacuum up on the higher floor I felt I was going to pull it down on top of me as the tube was not very flexible - maybe the more it is used the tube will loosen up a bit but at the beginning it was very taught and did not expand very easily. You had to basically extend it all the way out before using it and sometimes it would still coil back up. I would suggest not placing it above you if you are using it on steps.</p>
            <p>Cord could be longer. Found myself plugging in more outlets versus my Oreck.</p>
            <p>In summary: Positives: - Easy to put together - `snap` pieces are brilliant - Awesome on carpet - unbelievable suction - Maneuvers very easily with the ball - Easier to push around - less fatiguing on arm and shoulder</p>
            <p>Negatives: - A little heavy but that is only compared to my very light Oreck - Extension tube is very stiff and will pull the vacuum when you use it - not suggested to put above you as it could pull it down on you. Hopefully this loosens up with use. - Cord could be longer but not a big deal</p>
            <p>Overall - on a scale from 1-10 (1 low/10 high) I would give it a 9. The negatives are not deal breakers and the increased ease of use and suction make up for any shortcomings. I would definitely recommend this vacuum.</p>
